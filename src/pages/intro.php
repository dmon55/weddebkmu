                        <h2 style="text-align:left;">
                            <span style="font-size:28px;"><u>What is "Touhou?"</u></span>
                        </h2>
                        <div style="text-align:left;">
                            Touhou is an extremely popular series of vertical shooting games of the "bullet hell" or "curtain fire" variety, also known as "danmaku," where enemies not only shoot directly at you, but also fire patterned bullets to weave through.
                        </div>
                        <div style="text-align:left;">
                            Created by Zun, the Touhou game series entails an ongoing story of events set in the fictional realm of Gensokyo, revolving mostly -though not entirely - around incidents regarding the main character, Reimu Hakurei, and the Hakurei Shrine.
                        </div>
                        <div style="text-align:left;">
                            The series is best-known for its beautiful bullet patterns, seamless story development, and intriguing character interactions; like typical RPGs, character personalities are well-developed, considering the game type, and the dialogue is infused with both drama and humor, making Touhou fun not only in the combat aspect, but also interesting to watch unfold.<br>
                            &nbsp;
                        </div>
                        <h2 style="text-align:left;">
                            <u><span style="font-size:26px;">Aren't these in Japanese?</span></u>
                        </h2>
                        <div style="text-align:left;">
                            Originally, yes.&nbsp; However, we save you some trouble by pre-applying English patches when they are available, especially when it comes to the official games.
                        </div>
                        <div style="text-align:left;">
                            (They also come ready-to-play; no need to use potentially confusing methods to use an .iso file to install them.)<br>
